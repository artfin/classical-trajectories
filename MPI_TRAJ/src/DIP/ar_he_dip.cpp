		t5 = exp(-0.7458580635e-19 * t3);
		t6 = R * R;
		t7 = t6 * t6;
		t8 = t7 * t7;
		t9 = t8 * t7;
		t14 = pow(-0.27e2 + 0.2e1 * R, 0.2e1);
		t18 = exp(-0.2500000000e0 * t14 / t6);
